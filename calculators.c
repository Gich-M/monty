#include "monty.h"

/**
* add - adds the top two elements of the stack
* @head: pointer to top of the stack
* @counter: line count
*
* Return: nothing
*/
void add(stack_t **head, unsigned int counter)
{
	stack_t *h;
	int length = 0, temp;

	h = *head;
	while (h)
	{
		h = h->next;
		length++;
	}
	if (length < 2)
	{
		fprintf(stderr, "L%d: can't add, stack too short\n", counter);
		fclose(bus.file);
		free(bus.content);
		free_stack(*head);
		exit(EXIT_FAILURE);
	}
	h = *head;
	temp = h->n + h->next->n;
	h->next->n = temp;
	*head = h->next;
	free(h);
}

/**
* sub - subtracts the top element of the
* stack from the second top element of the stack
* @head: pointer to top of the stack
* @counter: line count
*
* Return: nothing
*/
void sub(stack_t **head, unsigned int counter)
{
	stack_t *temp;
	int sub, nodeCount;

	temp = *head;
	for (nodeCount = 0; temp != NULL; nodeCount++)
		temp = temp->next;
	if (nodeCount < 2)
	{
		fprintf(stderr, "L%d: can't sub, stack too short\n", counter);
		fclose(bus.file);
		free(bus.content);
		free_stack(*head);
		exit(EXIT_FAILURE);
	}
	temp = *head;
	sub = temp->next->n - temp->n;
	temp->next->n = sub;
	*head = temp->next;
	free(temp);
}

